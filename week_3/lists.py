items=["pen", "ruler", "set", "laptop",]
print(items)

#append --> used to add an item in a list
items.append("phone")
print("\n")
print(items)

#insert
items.insert(0, "eraser")
print("\n")
print(items)


#remove
items.remove("pen")
print("\n")
print(items)

#pop
items.pop()
print("\n")
print(items)

#index
index_of_eraser=items.index("eraser")
print("\n")
print(index_of_eraser)

#count
count_of_set=items.count("set")
print("\n")
print(count_of_set)

#sort
items.sort()
print("\n")
print(items)

#reverse
items.reverse()
print("\n")
print(items)




